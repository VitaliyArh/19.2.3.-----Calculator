class Calculator:
   def multiply(self, x, y):         # метод умножения
       return x * y

   def division(self, x, y):         # метод деления
       return x / y

   def subtraction(self, x, y):      # метод вычитания
       return x - y

   def adding(self, x, y):           # метод сложения
       return x + y

   # калькулятор готов --- теперь его можно использовать в тестах
   # для этого саздаём папку -- tests -- а в ней файл с тестами